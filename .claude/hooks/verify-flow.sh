#!/usr/bin/env bash

# ============================================================
# verify-flow.sh — 作業後自動検証フロー
# 実行順序:
#   1. codegen          — OpenAPI スペック生成 → 型定義生成
#   2. type-check       — 型整合性（codegen 後の型を含む）
#   3. test:unit        — 単体・コンポーネントテスト（モック）
#   4. test:api         — バックエンド API テスト（モック）
#   5. test:integration — 統合テスト（実 DB）※ DB 起動時のみ
#   6. build            — フロントエンド静的解析
# ひとつでも失敗した時点で処理を中断し、コミットを防止する
# ============================================================

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
cd "$REPO_ROOT"

echo ""
echo "========================================="
echo "  verify-flow: 作業後品質確認チェック"
echo "========================================="
echo ""

# ------------------------------------------------------------------
# 1. OpenAPI スペック生成 → 型定義生成（codegen）
# BE のルート定義と FE の型定義の整合性を保証する
# ------------------------------------------------------------------
echo "⚙️  [1/6] API 定義整合性チェック (pnpm run codegen)..."
if ! pnpm run codegen; then
    echo ""
    echo "❌ codegen が失敗しました。OpenAPI スペックと型定義の整合性を確認してください。"
    exit 1
fi
echo "✅ codegen: OK"

# ------------------------------------------------------------------
# 2. 型整合性チェック（codegen 後の型を含む）
# ------------------------------------------------------------------
echo ""
echo "🔍 [2/6] 型整合性チェック (pnpm run type-check)..."
if ! pnpm run type-check; then
    echo ""
    echo "❌ 型エラーが検出されました。コミットを中止します。"
    exit 1
fi
echo "✅ 型チェック: OK"

# ------------------------------------------------------------------
# 3. 単体・コンポーネントテスト（モック）
# ------------------------------------------------------------------
echo ""
echo "🧪 [3/6] 単体・コンポーネントテスト (pnpm test:unit)..."
if ! pnpm test:unit; then
    echo ""
    echo "❌ ユニットテストが失敗しました。コミットを中止します。"
    exit 1
fi
echo "✅ ユニットテスト: OK"

# ------------------------------------------------------------------
# 4. バックエンド API テスト（モック）
# ------------------------------------------------------------------
echo ""
echo "🌐 [4/6] バックエンド API テスト (pnpm test:api)..."
if ! pnpm test:api; then
    echo ""
    echo "❌ API テストが失敗しました。コミットを中止します。"
    exit 1
fi
echo "✅ API テスト: OK"

# ------------------------------------------------------------------
# 5. 統合テスト（実 DB）— DB コンテナが起動中の場合のみ実行
# docker-compose.test.yml の db_test (port 3307) を確認する
# ------------------------------------------------------------------
echo ""
echo "🗄️  [5/6] 統合テスト（実 DB）の確認..."
if nc -z 127.0.0.1 3307 2>/dev/null; then
    echo "  → テスト DB (port 3307) が起動中。統合テストを実行します..."
    if ! pnpm test:integration; then
        echo ""
        echo "❌ 統合テストが失敗しました。コミットを中止します。"
        exit 1
    fi
    echo "✅ 統合テスト: OK"
else
    echo "  ⚠️  テスト DB (port 3307) が未起動のため統合テストをスキップします。"
    echo "     実行する場合: docker compose -f docker-compose.test.yml up -d"
fi

# ------------------------------------------------------------------
# 6. フロントエンドビルド静的解析
# ------------------------------------------------------------------
echo ""
echo "🏗️  [6/6] フロントエンドビルド静的解析 (pnpm build)..."
if ! pnpm build; then
    echo ""
    echo "❌ ビルドが失敗しました。コミットを中止します。"
    exit 1
fi
echo "✅ ビルド: OK"

echo ""
echo "============================================================"
echo "✅ 全チェック通過。コミット可能です。"
echo "============================================================"
exit 0
