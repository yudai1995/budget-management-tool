#!/usr/bin/env bash
# ============================================================
# setup.sh — 初回セットアップスクリプト
# 使用方法: pnpm run setup
# 実行内容:
#   1. 依存関係のインストール（pnpm install）
#   2. .env の自動生成（.env.example からのコピー）
#   3. JWT RSA 鍵ペアの生成（gen:keys でプレースホルダーを実値に置換）
#   4. 共通パッケージのビルド（@budget/common）
#   5. DB コンテナの起動と healthcheck 待機
#   6. DB マイグレーションの実行
#   7. Seed データの投入
# ============================================================

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$REPO_ROOT"

echo ""
echo "========================================="
echo "  Budget Management Tool — セットアップ"
echo "========================================="

# ------------------------------------------------------------------
# 1. 依存関係のインストール
# ------------------------------------------------------------------
echo ""
echo "[1/7] 依存関係をインストールしています..."
pnpm install
echo "  ✅ インストール完了"

# ------------------------------------------------------------------
# 2. .env の生成（存在しない場合のみ）
# ------------------------------------------------------------------
echo ""
echo "[2/7] 環境変数ファイルを確認しています..."
if [ ! -f .env ]; then
  cp .env.example .env
  echo "  → .env を生成しました（.env.example からコピー）"
else
  echo "  → .env は既に存在します（スキップ）"
fi

# ------------------------------------------------------------------
# 3. JWT RSA 鍵ペアの生成
#    .env のプレースホルダー（"..."）を実際の鍵で置換する
#    有効な鍵が既に設定されている場合はスキップ（冪等）
# ------------------------------------------------------------------
echo ""
echo "[3/7] JWT 鍵ペアを生成しています..."
pnpm run gen:keys
echo "  ✅ JWT 鍵ペア設定完了"

# ------------------------------------------------------------------
# 4. 共通パッケージのビルド（@budget/common）
#    ts-node + tsconfig-paths が dist/ を参照するため、フレッシュクローン後に必須
# ------------------------------------------------------------------
echo ""
echo "[4/7] 共通パッケージをビルドしています..."
pnpm --filter @budget/common run build
echo "  ✅ @budget/common ビルド完了"

# ------------------------------------------------------------------
# 5. DB コンテナの起動と healthcheck 待機
# ------------------------------------------------------------------
echo ""
echo "[5/7] データベースを起動しています..."
docker compose up -d

echo "  → DB の起動を待機中（healthcheck）..."
TIMEOUT=60
ELAPSED=0
until docker inspect --format='{{.State.Health.Status}}' budget_db 2>/dev/null | grep -q "healthy"; do
  if [ "$ELAPSED" -ge "$TIMEOUT" ]; then
    echo ""
    echo "  ❌ DB の起動がタイムアウトしました（${TIMEOUT}秒）"
    echo "     docker compose logs db でログを確認してください"
    exit 1
  fi
  printf "."
  sleep 2
  ELAPSED=$((ELAPSED + 2))
done
echo ""
echo "  ✅ DB が正常に起動しました"

# ------------------------------------------------------------------
# 6. DB マイグレーションの実行
# ------------------------------------------------------------------
echo ""
echo "[6/7] マイグレーションを実行しています..."
pnpm --filter @budget/api run migration:run
echo "  ✅ マイグレーション完了"

# ------------------------------------------------------------------
# 7. Seed データの投入（ゲストユーザー等、アプリ起動に必須なレコード）
# ------------------------------------------------------------------
echo ""
echo "[7/7] Seed データを投入しています..."
pnpm --filter @budget/api run seed
echo "  ✅ Seed 完了"

echo ""
echo "========================================="
echo "  ✅ セットアップが完了しました！"
echo ""
echo "  開発を開始するには:"
echo "    pnpm dev"
echo "========================================="
